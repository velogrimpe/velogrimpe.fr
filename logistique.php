    <h1 align="center">ASPECTS PRATIQUES</h1>

    <p>
      <i>
        Le but de cette section est d’essayer de répondre aux questions que vous pourriez vous poser à propos du
        vélogrimpe : train, matériel, logistique, etc.
      </i>
    </p>

    <h2>Comment prendre le train avec mon vélo ?</h2>
    <p>
      Les TER acceptent <span class="underline">gratuitement</span> les vélos, dans la limite des places disponibles...
      Donc oui, si le TER est
      blindé, vous ne pourrez pas rentrer avec votre vélo. Ça arrive, mais c’est rare.
    </p>
    <p>Attention :</p>
    <ul>
      <li>Les tandems, fatbikes, vélocargos, remorques ne sont pas toujours autorisés dans les TER.</li>
      <li>Sur certaines lignes, à certaines périodes, une réservation est obligatoire ! C’est par exemple le cas
        les week-ends et jours fériés du 01/05/24 au 29/09/24 sur les lignes Valence-Die-Gap, Lyon-
        Genève, Lyon-Avignon-Marseille, Grenoble-Gap. C’est parait-il le cas tous les jours de l’année sur
        Lyon-Mâcon.</li>
    </ul>
    <p>
      Les TER ont le plus souvent des wagons prévus pour accrocher les vélos, qui sont généralement indiqués
      par des autocollants de vélos sur les portes correspondantes. On trouve souvent des crochets pour la
      roue avant, afin de suspendre verticalement le vélo. Parfois, c’est un espace dans lequel on peut appuyer
      quelques vélos les uns contre les autres. Dans d’autres cas, rien n’est prévu, ou alors tous les
      emplacements sont déjà pris, et avoir déjà joué à Tetris devient alors un avantage certain.
    </p>
    <p>
      Il est conseillé d’enlever les sacoches avant de monter dans le train, et de les porter en bandoulière, c’est
      plus pratique.
    </p>
    <p>
      Prendre le TGV avec un vélo est plus compliqué : il faut parfois démonter son vélo et/ou le mettre sous
      housse, et c’est parfois payant.
    </p>

    <h2>Les cartes de réduction</h2>
    <p>
      Selon les régions, il existe des cartes de réduction. Dans la région AURA, la carte illico liberté permet de
      bénéficier de réductions sur les TER de la région AURA :
    </p>
    <ul>
      <li>25% en semaine.</li>
      <li>50% les week-ends et jours fériés.</li>
      <li>Ces deux réductions marchent aussi pour un billet de TER connectant la région AURA à une région
        limitrophe <span class="text-sm">(exemple : les réductions s’appliquent sur Lyon-Marseille (25€), Lyon-Paris
          (32€),…).</span></li>
      <li>Possibilité de faire bénéficier jusqu’à trois accompagnants de la réduction de 50% les week-ends
        et jours fériés, seulement pour les sections en AURA</li>
    </ul>
    <p>
      Cette carte est vendue en gare, mais aussi sur le site internet TER AURA (depuis 2024), au prix de 30€/an.
      Mais surveillez le site internet : au moins un mois par an (souvent l’été), la carte est soldée à 15€/an.
    </p>
    <p>Autant dire qu’on rentabilise cette carte très vite !</p>
    <p>
      Il existe aussi la carte illico solidaire pour les personnes à la situation financière précaire (sans emploi,
      RSA,…), gratuite et encore plus avantageuse, et la carte illico jeunes pour les moins de 26 ans, moins
      chère et plus avantageuse que Illico liberté.
    </p>

    <h2>Quelques astuces pour organiser son voyage en train</h2>
    <ul>
      <li>On peut se référer à <a
          href="https://mmt.vsct.fr/sites/default/files/swt/CARA/2021-11/Carte_reseau_TER_Auvergne-Rhone-Alpes_40x60_1.pdf">la
          carte du réseau</a>, pour voir où sont les lignes et les gares.<br>
        Voir aussi <a href="/">la carte interactive du topo</a>, moins exhaustive, mais plus lisible.</li>
      <li>Le site et l’appli SNCF Connect ne donnent pas tous les trains existants, et blacklistent souvent les
        TER sur les longs trajets. On peut utiliser un autre planificateur d’itinéraire ; moi j’utilise <a
          href="https://hafas.bene-system.com/bin/query.exe/en?L=ns_hispeed&protocol=https:">celui-ci</a>.</li>
    </ul>

    <h2>Comment trimballer toutes mes affaires sur mon vélo ?</h2>
    <p>
      Certes, vous pouvez faire du vélo en portant un sac à dos de 15 kg…mais ce n’est pas ainsi que vous
      prendrez plaisir à pratiquer le vélogrimpe ! Si vous tenez vraiment à porter un sac à dos, chargez-le le moins
      possible…il vous faudra donc mettre vos affaires ailleurs !
    </p>
    <h3>Dans quoi mettre mes affaires ?</h3>
    <p>
      Le plus classique est d’installer un porte-bagages arrière, pour y positionner deux sacoches. Il reste
      encore la place d’accrocher un sac à dos (ou une corde !) sur le porte-bagages, avec l’aide d’un tendeur
      (voir ci-contre). Pour partir à la journée, ça suffit largement. Si on veut partir sur plusieurs jours, avec du
      matériel de bivouac, c’est possible aussi, mais on risque de se retrouver avec beaucoup de poids à
      l’arrière. Pour mieux répartir la charge, on peut envisager d’installer un porte-bagages avant
      supplémentaire, pour y accrocher deux autres sacoches.
    </p>
    <div class="flex flex-col md:flex-row gap-4 justify-center items-center">
      <img src="/images/logistique_0.jpg" alt="Vélo chargé à l'arrière" class="w-full md:w-1/2" />
      <img src="/images/logistique_1.jpg" alt="Saccoches avant et arrière" class="w-full md:w-1/2" />
    </div>
    <p>
      <i>
        A gauche : départ pour 3 jours (avec 2 bivouacs), sans chercher à faire light. Je porte tout mon matériel
        perso + corde de 80m + 25 dégaines, ma binôme porte le matériel de bivouac (tente, popote, gaz,…). Ça
        passe, mais beaucoup de poids à l’arrière.
      </i>
    </p>
    <p>
      <i>
        A droite : départ pour 2 jours (avec 2 bivouacs), sans chercher à faire light. Je porte tout mon matériel perso,
        le matériel de bivouac (tente, popote,…), la corde de 80m et 25 dégaines. Ce coup-ci, en plus des
        sacoches arrière 2x20L, j’ai des sacoches avant 2x12,5L, ce qui permet d’avoir un sac à dos à l’arrière quasi
        vide. C’est plus stable.
      </i>
    </p>
    <p>A noter qu’il existe d’autres types de contenants que les sacoches avant/arrière :</p>
    <ul>
      <li>Sacoches de selle, de cadre et de guidon (voir ci-dessous) : leur volume est moindre, mais on peut
        les prendre en complément.</li>
      <li>Panier à fixer sur le guidon : pas recommandé pour porter de lourdes charges.</li>
    </ul>
    <div class="flex flex-col md:flex-row gap-4 justify-center items-center">
