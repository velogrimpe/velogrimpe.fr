    <h3>Le choix du porte-bagages</h3>
    <p>Lorsque vous choisissez un porte-bagages :</p>
    <ul>
      <li>Vérifiez la charge maximale qu’il peut supporter, et choisissez selon votre utilisation (quel poids de
        bagages portez-vous ? Est-il envisagé d’y fixer un siège bébé ?).</li>
      <li>Soyez sûrs que votre porte-bagages puisse se fixer sur votre vélo. Il existe de nombreux moyens de
        fixation : souvent, des œillets percés dans le cadre permettent de l’y visser directement. Si ces
        œillets sont absents, on peut trouver des modèles de porte-bagages qui se fixent ailleurs (tige de
        selle, haubans, étriers de frein, fourche…), parfois à l’aide de colliers. Dans le doute, prenez le vélo
        avec vous dans le magasin.</li>
    </ul>
    <p>Prix neuf : à partir de 30€ pour des portes bagages solides.</p>
    <h3>Le choix des sacoches</h3>
    <p>Voici les questions à se poser avant l’achat :</p>
    <ul>
      <li>Quel volume ? Le standard est de 20L/sacoche pour l’arrière, 12,5L/sacoche à l’avant.</li>
      <li>Etanches ou pas ? C’est plus cher, mais c’est pratique…à vous de voir </li>
      <li>Quelle solidité ? Si vos sacoches sont destinées à trimballer votre quincaillerie pour grimper,
        privilégiez des sacoches solides, histoire qu’elles ne se déchirent pas en rase campagne.</li>
      <li>Sont-elles compatibles avec mon porte-bagages ? Elles sont vendues avec des réducteurs de
        diamètre en plastique, pour être sûr que le crochet de fixation de la sacoche soit compatible avec
        le diamètre des tiges du porte-bagages. Soyez vigilants si vous achetez d’occasion.</li>
      <li>Les sacoches ont-elles des bandoulières ? C’est plus pratique.</li>
    </ul>
    <p>Prix neuf : 30€ la paire de sacoches arrière pour le premier prix, et jusqu’à 150€ la paire pour des sacoches
      arrières étanches haut de gamme.</p>
    <h3>Peut-on louer tout cet équipement ?</h3>
    <p>Oui. A Lyon, le magasin <a href="https://velostrada.com/">Velostrada</a>, spécialisé dans les voyages à vélo,
      permet de louer tout ceci. En 2024,
      la location d’un vélo coûte 58€ pour 2j, et 155€ pour une semaine. La paire de sacoches arrière coûte 25€
      la semaine.</p>

    <h2>Comment accrocher son vélo (et ses sacoches), si on ne peut pas l’emmener à la falaise ?</h2>
    <p>
      Pour ma part, j’accroche mon vélo à un arbre, dans un coin discret si possible. De manière générale,
      privilégiez un bon cadenas (chaîne ou U ; la chaîne permet un choix d’arbres plus large). Si vous êtes
      plusieurs, vous pouvez choisir d’accrocher tous vos vélos les uns aux autres, dans une sorte de chaos
      inextricable.
    </p>
    <p>
      Je mets les affaires nécessaires à la grimpe dans le sac à dos que je trimballais sur le porte-bagages, et je
      monte à la falaise avec. Concernant les sacoches et le matériel inutile pour grimper, soit je les attache au
      vélo avec le cadenas, soit je les cache dans la forêt.
    </p>

    <h2>Et si j’ai un problème de mécanique vélo pendant ma sortie ?</h2>
    <p>
      Forcément, quand on décide de se déplacer sur deux roues, on devient dépendant de son vélo… Il est bon
      de savoir bricoler un minimum, ou de partir avec quelqu’un qui sait !
    </p>
    <p>
      A mon avis, il faut au grand minimum savoir changer une chambre à air, et remettre une chaîne qui a
      déraillé. Pour les autres problèmes, on peut essayer de bricoler quelque chose de plus ou moins
      temporaire (même si on ne sait a priori pas faire, on peut parfois se surprendre soi-même !), ou rouler
      prudemment jusqu’à la maison si le vélo le permet, ou encore compter sur les gens sur place
      (automobilistes, habitants…).
    </p>
    <p>
      Prenez avec vous de quoi bricoler un minimum : tournevis plat, cruciforme, clefs allen, démonte pneu,
      pompe, chambre à air et/ou rustines, scotch, ficelle, clefs plates
    </p>
    <p>
      Vous pouvez apprendre la base de la mécanique vélo en suivant des tutos sur Internet, ou en vous rendant
      dans un des <a href="https://clavette-lyon.heureux-cyclage.org/">ateliers d’autoréparation lyonnais</a>.
    </p>

    <h2>Quel matériel emporter dans une sortie train + vélo + grimpe ?</h2>
    <p>PARTIE TRAIN : billet, carte de réduction. Avoir réservé pour le vélo si besoin.</p>
    <p>PARTIE VÉLO :</p>
    <ul>
      <li>Vélo + porte-bagages + sacoches arrière.</li>
      <li>Casque.</li>
      <li>Tendeur(s).</li>
      <li>Sac à dos à fixer sur le porte-bagages.</li>
      <li>Cadenas (pas trop petit, sinon c’est impossible de faire le tour d’un arbre).</li>
      <li>Matériel de réparation. On pourrait en parler des heures, et ça dépend du vélo, mais au minimum
        de quoi réparer une crevaison (pompe, démonte pneus, clef plate pour démonter la roue si
        nécessaire, chambre à air de rechange ou pack rustines). A ça, je rajouterais des clefs allen de 3 à
        6 mm, clefs plates nécessaires à votre vélo, tournevis plat/cruciforme. Peut-être un dérive-chaîne.
        Scotch solide et ficelle.</li>
      <li>Optionnel : autre contenant (sacoche avant, panier…), gourde.</li>
    </ul>
    <p>PARTIE GRIMPE : ça, je vous laisse vous débrouiller ;)</p>

    <h2>Et pour partir sur plusieurs jours ?</h2>
    <p>On l’a vu plus haut, c’est faisable aussi, en acceptant de trimballer un peu plus de matériel.</p>
    <p>
      Si vous souhaitez bivouaquer, assurez-vous de rester discret ; trop de bivouacs à proximité des falaises
      peuvent créer des tensions avec les riverains, et aboutir à une interdiction de grimper.
    </p>

    <h2>Comment suivre un itinéraire à vélo ?</h2>
    <p>
      Vous pouvez planifier cet itinéraire depuis chez vous, puis le suivre soit à l’aide d’une carte papier
      suffisamment précise, soit avec votre téléphone.
    </p>
    <p>
      Si vous décidez de vous orienter avec votre téléphone, vous pouvez vous contenter de regarder la carte sur
      une appli quelconque, mais vous pouvez aussi y superposer la trace GPS de l’itinéraire (récupérée en
      amont sur ce topo, ou réalisée par vos soins). Il existe de nombreuses applis permettant d’afficher des
      traces GPS sur des fonds de cartes, je vous laisse faire votre choix. Mes traces sont conçues sous
      Openrunner.
    </p>

    <h2>Le train et le vélo, c’est beaucoup plus écolo que la voiture ?</h2>
    <p>
      Encore faudrait-il définir le mot « écolo ». Intéressons-nous simplement aux émissions de gaz à effet de
      serre. Voici les chiffres de l’ADEME, prenant en compte la fabrication, l’utilisation et la fin de vie des
      différents moyens de transport, mais pas les infrastructures sur lesquelles ils circulent :
    </p>
    <ul>
      <li><b>Vélo : 0 g/km</b> <i class="text-xs">(le chiffre de ≈5 g/km me semblerait plus crédible !)</i></li>
      <li>Vélo électrique : 10,9 g/km <i class="text-xs">(dont 80% pour la fabrication du vélo, 20% pour l’électricité
          de l’utilisation).</i></li>
      <li><b>TER électrique* : 8,91 g/km/passager.</b></li>
      <li>TER gazole* : 79,8 g/km/passager.</li>
      <li><b>Voiture : 231g/km</b> <i class="text-xs">(dont 18% pour la fabrication, 82% pour le carburant)</i></li>
    </ul>
    <p>
      <i class="text-sm">
        * Pour savoir quelles lignes TER sont électrifiées, on peut se référer à <a
          href="https://www.openrailwaymap.org/?style=electrified&lat=45.25362179991922&lon=5.10589599609375&zoom=8">cette
          carte d’openrailwaymap</a>.
      </i>
    </p>
